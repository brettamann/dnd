package com.masters_of_destiny.Ai.People.Statistics;

public class Spiritual {
    public int base;
    public int attunement; // power/duration of spells, chance to succeed
    public int capacity; // uses of spells per day
    public int maturity; // requirement for high level spells
    public int fortitude; // allows resistance of magical effects
    public int sensitivity; // identifying and detecting magic
    public int mastery; // affects casting time and cooldown.
}
